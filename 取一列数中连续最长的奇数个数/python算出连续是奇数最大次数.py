a=[2,3,3,0,0,2,4,7,5,7,324,4234,345,353,5444,55,45,9,8,69,459,469,9]
print(max([len(r) for r in (''.join([str(0) if (i%2 ==0) else str(1) for i in a]).split("0"))]))
